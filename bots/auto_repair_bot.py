"""
Auto Repair Bot — Automatically attempts to fix common CI and code failures.

Reads recent failure logs, matches them against the ranked knowledge base,
and applies safe automated patches (e.g. missing imports, wrong paths,
missing __init__.py files) without human intervention.

Usage
-----
    python bots/auto_repair_bot.py
"""

from __future__ import annotations

import json
import os
import sys
import time

sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), "..")))

_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), ".."))
FEEDBACK_LOG = os.path.join(_ROOT, "knowledge", "feedback_log.json")
RANKED_FILE = os.path.join(_ROOT, "knowledge", "ranked_insights.json")
REPAIR_LOG = os.path.join(_ROOT, "knowledge", "repair_log.json")

_SAFE_REPAIRS: list[dict] = [
    {
        "pattern": "__init__",
        "description": "Add missing __init__.py",
        "action": "ensure_init_py",
    },
    {
        "pattern": "module not found",
        "description": "Ensure sys.path includes repo root",
        "action": "check_sys_path",
    },
    {
        "pattern": "requirements",
        "description": "Verify requirements.txt is complete",
        "action": "check_requirements",
    },
]


def _load(path: str) -> list[dict]:
    try:
        with open(path) as fh:
            return json.load(fh)
    except (OSError, json.JSONDecodeError):
        return []


def _save(data: list[dict], path: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as fh:
        json.dump(data, fh, indent=2)


def _ensure_init_py(target_dir: str) -> bool:
    """Create __init__.py in *target_dir* if missing."""
    init_path = os.path.join(target_dir, "__init__.py")
    if not os.path.isfile(init_path):
        with open(init_path, "w") as fh:
            fh.write("# Auto-generated by auto_repair_bot\n")
        return True
    return False


def detect_issues(feedback_log: list[dict]) -> list[dict]:
    """Identify actionable issues from the feedback log."""
    issues: list[dict] = []
    for entry in feedback_log[-50:]:
        if entry.get("status") not in ("error", "warning"):
            continue
        error_text = (entry.get("error", "") or "").lower()
        for repair in _SAFE_REPAIRS:
            if repair["pattern"] in error_text:
                issues.append(
                    {
                        "bot": entry.get("bot", "unknown"),
                        "error": error_text[:200],
                        "repair": repair,
                        "timestamp": entry.get("timestamp", ""),
                    }
                )
    return issues


def apply_repair(issue: dict) -> dict:
    """Apply the safe repair described in *issue*.

    Returns
    -------
    dict
        Result with status and description.
    """
    action = issue["repair"]["action"]
    result = {
        "bot": issue["bot"],
        "action": action,
        "description": issue["repair"]["description"],
        "applied_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "status": "skipped",
    }

    if action == "ensure_init_py":
        bots_dir = os.path.join(_ROOT, "bots")
        created = _ensure_init_py(bots_dir)
        result["status"] = "applied" if created else "already_exists"

    elif action == "check_sys_path":
        result["status"] = "manual_review_needed"
        result["note"] = "Ensure sys.path.insert(0, repo_root) at top of bot file."

    elif action == "check_requirements":
        req_path = os.path.join(_ROOT, "requirements.txt")
        result["status"] = "ok" if os.path.isfile(req_path) else "requirements_missing"

    return result


def run_repairs() -> dict:
    """Detect and apply all safe repairs. Returns a full repair report."""
    feedback = _load(FEEDBACK_LOG)
    issues = detect_issues(feedback)
    repair_results: list[dict] = []

    for issue in issues:
        repair_result = apply_repair(issue)
        repair_results.append(repair_result)

    # Persist repair log
    repair_log = _load(REPAIR_LOG)
    repair_log.extend(repair_results)
    _save(repair_log[-200:], REPAIR_LOG)

    applied = sum(1 for r in repair_results if r["status"] == "applied")
    return {
        "issues_found": len(issues),
        "repairs_applied": applied,
        "results": repair_results,
        "status": "repaired" if applied else "no_repairs_needed",
    }


def run(context: dict | None = None) -> dict:
    """Entry point for the Task Execution Controller."""
    return run_repairs()


if __name__ == "__main__":
    report = run_repairs()
    print(json.dumps(report, indent=2))
    print(
        f"\n🔧 {report['issues_found']} issue(s) found, "
        f"{report['repairs_applied']} repair(s) applied."
    )
